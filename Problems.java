// Write a Java program to print the sum (addition), multiply, subtract, divide and remainder of two numbers. 
class Problems
{
public static void main(String []args)
{
System.out.println("The Two Number is :");
int a = 125;
int b = 24;
System.out.println(a + "," + b);
int sum = a+b;
System.out.println("The sum is:"+a+"+"+b+"="+sum);
int subtract = a-b;
System.out.println("The subtract is:"+a+"-"+b+"="+subtract);
int multiply = a*b;
System.out.println("The multiply is:"+a+"*"+b+"="+multiply);
int divide = a/b;
System.out.println("The divide is:"+a+"/"+b+"="+divide);
int mod = a%b;
System.out.println("The mod is:"+a+"%"+b+"="+mod);
}
}
